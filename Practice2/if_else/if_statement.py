# If Statement in Python
# Use 'if' to execute code when a condition is true

# Example 1: Basic if statement
a = 33
b = 200
if b > a:
    print("b is greater than a")

# Example 2: If with equality check
x = 10
if x == 10:
    print("x is equal to 10")

# Example 3: If with variable
is_raining = True
if is_raining:
    print("Take an umbrella!")

# Example 4: Multiple conditions in if
age = 20
if age >= 18:
    print("You are an adult")

# Example 5: If with string comparison
name = "Alice"
if name == "Alice":
    print("Hello, Alice!")
